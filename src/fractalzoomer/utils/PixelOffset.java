package fractalzoomer.utils;

public class PixelOffset {

    public PixelOffset() {
    }

    public int getX(int x) {
        return x;
    }

    public int getY(int y) {
        return y;
    }

    public int getWidth(int width) {
        return width;
    }
    public int getHeight(int height) {
        return height;
    }
}
