    public Formula51(double xCenter, double yCenter, double size, int max_iterations, ArrayList<Complex> complex_orbit, int plane_type, boolean apply_plane_on_julia, boolean apply_plane_on_julia_seed, double[] rotation_vals, double[] rotation_center, String user_plane, int user_plane_algorithm, String[] user_plane_conditions, String[] user_plane_condition_formula, double[] plane_transform_center, Apfloat[] plane_transform_center_hp, double plane_transform_angle, double plane_transform_radius, double[] plane_transform_scales, double[] plane_transform_wavelength, int waveType, double plane_transform_angle2, int plane_transform_sides, double plane_transform_amount, ArrayList<Double> inflections_re, ArrayList<Double> inflections_im, double inflectionsPower, Complex[] zenex_coefs, double xJuliaCenter, double yJuliaCenter) {
        super(xCenter, yCenter, size, max_iterations, complex_orbit, plane_type, apply_plane_on_julia, apply_plane_on_julia_seed, rotation_vals, rotation_center, user_plane, user_plane_algorithm, user_plane_conditions, user_plane_condition_formula, plane_transform_center, plane_transform_center_hp, plane_transform_angle, plane_transform_radius, plane_transform_scales, plane_transform_wavelength, waveType, plane_transform_angle2, plane_transform_sides, plane_transform_amount, inflections_re, inflections_im, inflectionsPower, xJuliaCenter, yJuliaCenter);
        this.zenex_coefs = zenex_coefs;
        pertur_val = new DefaultPerturbation();
        init_val = new InitialValue(0, 0);
    }

    @Override
    public void function(Complex[] complex) {
        Complex zsqr = complex[0].square();

        Complex expSum = zenex_coefs[2].times(zsqr);
        if(!zenex_coefs[3].isZero()) {
            expSum.plus_mutable(zenex_coefs[3].times(complex[0].fourth()));
        }
        if(!zenex_coefs[4].isZero()) {
            expSum.plus_mutable(zenex_coefs[4].times(complex[0].sixth()));
        }
        if(!zenex_coefs[5].isZero()) {
            expSum.plus_mutable(zenex_coefs[5].times(complex[0].eighth()));
        }
        if(!zenex_coefs[6].isZero()) {
            expSum.plus_mutable(zenex_coefs[6].times(complex[0].tenth()));
        }

        complex[0] = zenex_coefs[0].plus(zenex_coefs[1].times(zsqr)).times_mutable(expSum.exp()).plus_mutable(complex[1]);
    }

    @Override
    public boolean hasFiniteBailoutCheck() {
        return true;
    }
