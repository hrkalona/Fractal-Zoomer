    @Override
    protected GenericComplex referenceFunction(GenericComplex z, GenericComplex c, NormComponents normData, GenericComplex[] initialPrecal, GenericComplex[] precalc) {
        if(normData != null) {
            return z.squareFast(normData).times2_mutable().sub_mutable(z.cubeFast(normData)).plus_mutable(c);
        }
        else {
            return z.square().times2_mutable().sub_mutable(z.cube()).plus_mutable(c);
        }
    }

