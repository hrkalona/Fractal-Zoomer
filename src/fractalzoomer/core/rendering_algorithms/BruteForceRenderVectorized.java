    @Override
    protected void render(int image_width, int image_height, boolean polar) throws StopSuccessiveRefinementException, StopExecutionException {

        Location location = Location.getInstanceForRendering(xCenter, yCenter, size, height_ratio, image_width, image_height, circle_period, rotation_center, rotation_vals, fractal, js, polar, (PERTURBATION_THEORY || HIGH_PRECISION_CALCULATION) && fractal.supportsPerturbationTheory());

        //Better brute force
        int x, y, loc;

        int condition = image_width * image_height;

        initialize(location);

//        VectorSpecies<Double> DOUBLE_SPECIES = DoubleVector.SPECIES_PREFERRED;
//
//        int laneCount = DOUBLE_SPECIES.length();
        int laneCount = 1;

        boolean escaped_val;
        double f_val;
        int thread_chunk_size = THREAD_CHUNK_SIZE % laneCount == 0 ? THREAD_CHUNK_SIZE : THREAD_CHUNK_SIZE + (laneCount - (THREAD_CHUNK_SIZE % laneCount));

        GenericComplex[] vals = new GenericComplex[laneCount];

        long time = System.currentTimeMillis();

        do {

            loc = thread_chunk_size * normal_rendering_algorithm_pixel.getAndIncrement();

            if(loc >= condition) {
                break;
            }

            for(int count = 0; count < thread_chunk_size && loc < condition; count += laneCount, loc += laneCount) {

                for(int i = 0; i < laneCount; i++) {
                    int final_loc = loc + i;
                    y = final_loc/ image_width;
                    x = final_loc % image_width;
                    vals[i] = location.getComplex(x, y);
                }

                double[] f_vals = iteration_algorithm.calculateVectorized(vals);
                boolean[] escaped_vals = iteration_algorithm.vescaped();

                for(int i = 0; i < laneCount; i++) {
                    int final_loc = loc + i;
                    if(final_loc < rgbs.length) {
                        image_iterations[final_loc] = f_val = f_vals[i];
                        escaped[final_loc] = escaped_val = escaped_vals[i];
                        rgbs[final_loc] = getFinalColor(f_val, escaped_val);
                        task_calculated++;
                        rendering_done_per_task[taskId]++;
                    }
                }
            }

            updateProgress();

        } while(true);

        pixel_calculation_time_per_task = System.currentTimeMillis() - time;

        postProcess(image_width, image_height,null, location);
    }

